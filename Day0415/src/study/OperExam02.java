package study;

//시프트연산

public class OperExam02 {

	public static void main(String[] args) {
		
		int i = 10;
		int j = i >> 2;
		
		System.out.println(j); // 10(10)은 1010(₂)이다. 오른쪽으로 두칸 날리니까 10(₂)가 된다. 10(₂)는 2다.
		
		i = 10;
		j = i << 3;
		
		System.out.println(j); // 1010이 왼쪽으로 세칸. 1010000이 된다. 
		
		
		
		
		
		
		
	}

}
